<template>
  <div>
      <form @submit.prevent="hi">
          <input type="text" v-model="title" placeholder="Add todo">
          <button type="submit" value="Submit" class="btn">Add Todo</button>
      </form>
      
  </div>
</template>

<script>
//import { v4 as uuidv4 } from 'uuid';
export default {
    name:"AddTodo",
    data(){
        return{
            title:''
        }
    },
    methods:{
        hi(){
            const newTodo={
               
                title:this.title,
                completed:false
            }
        this.$emit("newTodo",newTodo);
        this.title='';
        }
    }
}
</script>

